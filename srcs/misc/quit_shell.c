#include "sh42.h"

void	quit_shell(t_core *shell, int exit_value, int8_t verbose)
{
	reset_config(shell, &(shell->cmd_line));
	if (verbose != 0)
		write(STDERR_FILENO, "exit\n", 5);
	exit(exit_value);
}
